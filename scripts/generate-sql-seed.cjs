const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');
const slidesPath = path.join(rootDir, 'config', 'slides.json');
const roomsPath = path.join(rootDir, 'config', 'roomConfigurations.json');
const outputPath = path.join(rootDir, 'migrations', 'sql', '002_seed_from_json.sql');

function escapeSqlString(value) {
  if (value === null || value === undefined) {
    return 'NULL';
  }
  return `N'${String(value).replace(/'/g, "''")}'`;
}

function toBit(value) {
  return value ? '1' : '0';
}

function buildSeedSql() {
  const slidesJson = JSON.parse(fs.readFileSync(slidesPath, 'utf8'));
  const roomsJson = JSON.parse(fs.readFileSync(roomsPath, 'utf8'));

  const slides = slidesJson.ALL_SLIDES || [];
  const rooms = roomsJson.configurations || [];
  const version = roomsJson.version || '1.0.0';
  const lastUpdated = roomsJson.lastUpdated || new Date().toISOString().slice(0, 10);

  const lines = [];
  lines.push('/*');
  lines.push('  Auto-generated by scripts/generate-sql-seed.cjs');
  lines.push('  Source: config/slides.json + config/roomConfigurations.json');
  lines.push('*/');
  lines.push('');
  lines.push('SET NOCOUNT ON;');
  lines.push('BEGIN TRANSACTION;');
  lines.push('');
  lines.push('DECLARE @DraftRelease UNIQUEIDENTIFIER = NEWID();');
  lines.push('DECLARE @PublishedRelease UNIQUEIDENTIFIER = NEWID();');
  lines.push('');
  lines.push('INSERT INTO dbo.ContentRelease (ReleaseId, VersionNumber, Stage, IsCurrent, Notes, CreatedBy)');
  lines.push(`VALUES (@DraftRelease, 1, N'draft', 1, N'Imported from JSON (${version} - ${lastUpdated})', N'migration-script');`);
  lines.push('');
  lines.push('INSERT INTO dbo.ContentRelease (ReleaseId, VersionNumber, Stage, IsCurrent, Notes, CreatedBy)');
  lines.push(`VALUES (@PublishedRelease, 1, N'published', 1, N'Imported from JSON (${version} - ${lastUpdated})', N'migration-script');`);
  lines.push('');

  for (const releaseVar of ['@DraftRelease', '@PublishedRelease']) {
    lines.push(`-- Slides for ${releaseVar}`);
    for (const slide of slides) {
      lines.push(
        [
          'INSERT INTO dbo.Slide',
          '(SlideKey, ReleaseId, SlideId, Title, Description, ImageUrl, VideoUrl, QnaEnabled, PitchScriptEnUs, PitchScriptEnUk, PitchScriptPtBr, SortOrder)',
          'VALUES',
          `(`,
          `  NEWID(),`,
          `  ${releaseVar},`,
          `  ${escapeSqlString(slide.id)},`,
          `  ${escapeSqlString(slide.title)},`,
          `  ${escapeSqlString(slide.description || '')},`,
          `  ${escapeSqlString(slide.imageUrl)},`,
          `  ${slide.videoUrl ? escapeSqlString(slide.videoUrl) : 'NULL'},`,
          `  ${toBit(slide.QnA === true)},`,
          `  ${escapeSqlString(slide.pitchScript?.['en-US'] || '')},`,
          `  ${escapeSqlString(slide.pitchScript?.['en-UK'] || '')},`,
          `  ${escapeSqlString(slide.pitchScript?.['pt-BR'] || '')},`,
          `  0`,
          ');',
        ].join('\n')
      );
    }
    lines.push('');
  }

  for (const releaseVar of ['@DraftRelease', '@PublishedRelease']) {
    lines.push(`-- Rooms for ${releaseVar}`);
    for (const room of rooms) {
      lines.push(
        [
          'INSERT INTO dbo.Room',
          '(RoomKey, ReleaseId, RoomId, Name, Description, PrefixCode, AudienceCode, ModuleCode, FocusCode, DefaultLanguage, MetadataJson)',
          'VALUES',
          `(`,
          `  NEWID(),`,
          `  ${releaseVar},`,
          `  ${escapeSqlString(room.roomId)},`,
          `  ${escapeSqlString(room.name)},`,
          `  ${escapeSqlString(room.description || '')},`,
          `  ${escapeSqlString(room.components?.prefix || null)},`,
          `  ${escapeSqlString(room.components?.audience || null)},`,
          `  ${escapeSqlString(room.components?.module || null)},`,
          `  ${escapeSqlString(room.components?.focus || null)},`,
          `  ${escapeSqlString(room.defaultLanguage || null)},`,
          `  ${escapeSqlString(JSON.stringify(room.metadata || {}))}`,
          ');',
        ].join('\n')
      );
    }
    lines.push('');
  }

  for (const releaseVar of ['@DraftRelease', '@PublishedRelease']) {
    lines.push(`-- Room slide ordering for ${releaseVar}`);
    for (const room of rooms) {
      (room.slideSequence || []).forEach((slideId, index) => {
        lines.push(
          [
            'INSERT INTO dbo.RoomSlide (RoomSlideId, ReleaseId, RoomKey, SlideKey, SequenceIndex)',
            'SELECT',
            `  NEWID(),`,
            `  ${releaseVar},`,
            '  r.RoomKey,',
            '  s.SlideKey,',
            `  ${index + 1}`,
            'FROM dbo.Room r',
            'JOIN dbo.Slide s ON s.ReleaseId = r.ReleaseId',
            `WHERE r.ReleaseId = ${releaseVar}`,
            `  AND r.RoomId = ${escapeSqlString(room.roomId)}`,
            `  AND s.SlideId = ${escapeSqlString(slideId)};`,
          ].join('\n')
        );
      });
    }
    lines.push('');
  }

  lines.push('COMMIT TRANSACTION;');
  lines.push('');

  return lines.join('\n');
}

function main() {
  const sql = buildSeedSql();
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, sql, 'utf8');
  console.log(`Generated: ${path.relative(rootDir, outputPath)}`);
}

main();
